    // 考点 11：TLB（快表）与多级页表（MMU 硬件翻译）
    for (int i = 0; i < ROW; i++) {
        for (int j = 0; j < COL; j++) {
            
            // 考点 12：分支预测与流水线“控制冒险”
            // 考点 13：数据相关（RAW/WAR/WAW）与流水线“数据冒险”
            if (src[i][j] > 0) {
                // 考点 14：变址寻址（IX + A）—— 数组遍历的底层硬核实现
                // 考点 15：CPU 内部数据通路（微操作控制信号：In/Out）
                dest[i][j] = func_ptr(src[i][j], step); 
            }
        }
    }

    // ==========================================
    // 🔴 锚点四：I/O 系统与总线体系
    // ==========================================
    // 考点 16：I/O 端口编址（统一编址 vs 独立编址）
    // 考点 17：总线定时协议（同步定时 vs 异步定时/全互锁握手）
    // 考点 18：程序中断方式 vs DMA 方式（周期挪用、中断向量表）
    printf("Tour finished. Result tag: %c\n", data_node.tag);
