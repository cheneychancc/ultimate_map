    int step = (int)u_data >> 31;

    // 考点 8：间接寻址 —— 通过寄存器/内存间接获取目标地址
    int (*func_ptr)(int, int) = compute_add;

    // ==========================================
    // 🔴 锚点三：存储体系、CPU 数据通路与流水线
    // ==========================================
